export function getAvailableLanguages() {
  return Object.keys(TRANSLATIONS).map(code => ({
    code,
    label:   TRANSLATIONS[code]?.meta?.label   || code.toUpperCase(),
    sttCode: TRANSLATIONS[code]?.meta?.sttCode || `${code}-${code.toUpperCase()}`,
  }));
}

export function t(lang, category, key, fallback = "en") {
  try {
    return TRANSLATIONS[lang]?.[category]?.[key]
      || TRANSLATIONS[fallback]?.[category]?.[key]
      || key;
  } catch (e) {
    console.error(`❌ [TRANSLATION] Error: ${lang}.${category}.${key}`, e);
    return TRANSLATIONS[fallback]?.[category]?.[key] || key;
  }
}

export function getTrans(lang) {
  return (category, key, overrideLang) => t(overrideLang || lang, category, key);
}